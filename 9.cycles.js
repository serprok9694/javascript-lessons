// * Циклы

// Цикл for

for (let i = 0; i < 3; i++) {   // let i = 0 - объявление переменной; i < 3 - условие выполнения цикла; i++ - действие после выполнения шага цикла
    console.log(i);                   // возвращает 0, затем 1, затем 2
};

// Цикл do while
let i = 0;
do {
    alert(i);
    i++;
} while (i < 3);                // условие выполнения цикла

for (let i = 0; i < 3; i++) {
    console.log(i);
};
